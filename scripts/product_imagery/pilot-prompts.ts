export function buildPilotPrompt(slug: string, slot: EditorialSlot): string {
  const contract = PRODUCT_CONTRACTS[slug as keyof typeof PRODUCT_CONTRACTS];
  if (!contract) {
    throw new Error(`Unknown pilot SKU: ${slug}`);
  }

  return `${SHARED_DIRECTION}\u4ea7\u54c1\u7ed3\u6784\u5408\u7ea6\uff1a${contract.identity}${getSlotDirection(contract, slot)}`;
}

function getSlugArgument(args: readonly string[]): string | undefined {
  const slugIndex = args.indexOf("--slug");
  return slugIndex === -1 ? undefined : args[slugIndex + 1];
}

function isCliInvocation(): boolean {
  const scriptPath = process.argv[1];
  return Boolean(scriptPath) && import.meta.url === pathToFileURL(scriptPath).href;
}

if (isCliInvocation()) {
  const slug = getSlugArgument(process.argv.slice(2));
  if (!slug || !(slug in PRODUCT_CONTRACTS)) {
    console.error("Usage: pilot-prompts.ts --slug <pilot-sku>");
    process.exitCode = 1;
  } else {
    console.log(JSON.stringify(EDITORIAL_SLOTS.map((slot) => ({ slot, prompt: buildPilotPrompt(slug, slot) })), null, 2));
  }
}
